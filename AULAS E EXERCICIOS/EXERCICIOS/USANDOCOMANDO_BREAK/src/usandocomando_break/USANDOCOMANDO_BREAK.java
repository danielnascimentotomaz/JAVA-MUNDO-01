
package usandocomando_break;

public class USANDOCOMANDO_BREAK {

    
    public static void main(String[] args) {
         int a ;
        a = 0;
        while(a < 10){
           
            if(a == 5){
                break;
            }
            System.out.println(a);
            a = a + 1;
        }
                    
    
    }
    
}
